let i = 1;

while (i <= 30) {
    if (i === 10) {
        console.log(`ten`);
    } else if (i === 20) {
        console.log(`twenty`);
    } else {
        console.log(`${i}`);
    }
    i++;
}



let j = 1;

while (j <= 20) {
    if (j % 2 === 0) {
        console.log(`${j} is even`);
    } else {
        console.log(`${j} is odd`);
    }
    j++;
}